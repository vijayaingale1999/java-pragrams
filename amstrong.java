import java.util.Scanner;

public class amstrong {
    //amstrong no is the positive m digit no that is equal to the sum of mth power of their digit
    public static void main(String[] args)
    {
        Scanner s=new Scanner(System.in);
        int i= s.nextInt();
        int j=i,count=0;
         while(j==0)
         {
          count++;
           j=j/10;
         }
        
    }
    
}
